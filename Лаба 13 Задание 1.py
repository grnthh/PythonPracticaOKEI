файл = open('C:/Users/d853/OneDrive/Рабочий стол/text.txt', 'r', encoding='utf-8')
текст = файл.read()
файл.close()
слова = текст.split()
частоты = {}
for слово in слова:
    слово = слово.strip('.,!?":;()').lower()
    if слово:
        частоты[слово] = частоты.get(слово, 0) + 1
for слово in sorted(частоты):
    print(f"{слово} - {частоты[слово]} шт.")
