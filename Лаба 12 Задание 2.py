количество = int(input("введите количество студентов: ").strip())
данные = {}
for _ in range(количество):
    строка = input("введите группу и фио: ").strip()
    части = строка.split()
    if len(части) < 4:
        continue
    группа = части[0]
    фамилия = части[1]
    имя = части[2]
    отчество = части[3]
    запись = f"{фамилия} {имя[0]}.{отчество[0]}."
    данные.setdefault(группа, []).append(запись)
for группа in sorted(данные):
    for запись in sorted(данные[группа]):
        print(группа, '\t', запись, sep='')
