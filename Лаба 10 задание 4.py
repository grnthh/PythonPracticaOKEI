n = int(input("введите количество студентов: "))
for _ in range(n):
    фио = input("введите инициалы студента полностью: ").split()
    фамилия = фио[0]
    инициалы = f"{фио[1][0]}.{фио[2][0]}."
    оценки = []
    print("введите оценки через enter (пустая строка — конец):")
    while True:
        строка = input()
        if строка == "":
            break
        оценки.append(int(строка))
    кол_5 = оценки.count(5)
    кол_4 = оценки.count(4)
    кол_3 = оценки.count(3)
    средний = sum(оценки) / len(оценки)
    print(f"{фамилия} {инициалы}\t{кол_5}\t{кол_4}\t{кол_3}\t{средний:.2f}")